package com.jd.answersearchtool.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.io.Serializable;

@Entity
public class Record implements Serializable {
    @Id
    private Integer id;
    @Column
    private String firstcategory;
    @Column
    private String secondcategory;
    @Column
    private String thirdcategory;
    @Column
    private String question;
    @Column
    private String answer;
    @Column
    private String webanswer;
    @Column
    private String h5answer;
    @Column
    private String formtype;
    @Column
    private String type;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getFirstcategory() {
        return firstcategory;
    }

    public void setFirstcategory(String firstcategory) {
        this.firstcategory = firstcategory;
    }

    public String getSecondcategory() {
        return secondcategory;
    }

    public void setSecondcategory(String secondcategory) {
        this.secondcategory = secondcategory;
    }

    public String getThirdcategory() {
        return thirdcategory;
    }

    public void setThirdcategory(String thirdcategory) {
        this.thirdcategory = thirdcategory;
    }

    public String getQuestion() {
        return question;
    }

    public void setQuestion(String question) {
        this.question = question;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public String getFormtype() {
        return formtype;
    }

    public void setFormtype(String formtype) {
        this.formtype = formtype;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWebanswer() {
        return webanswer;
    }

    public void setWebanswer(String webanswer) {
        this.webanswer = webanswer;
    }

    public String getH5answer() {
        return h5answer;
    }

    public void setH5answer(String h5answer) {
        this.h5answer = h5answer;
    }
}
